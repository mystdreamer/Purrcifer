using UnityEngine;

/// <summary>
/// Class for serializing item display text. 
/// </summary>
[System.Serializable]
public class ItemDialogue
{
    /// <summary>
    /// The name of the item. 
    /// </summary>
    public string itemDisplayName;

    /// <summary>
    /// The flavour text to accompany the item. 
    /// </summary>
    public string itemDisplayText;

}
